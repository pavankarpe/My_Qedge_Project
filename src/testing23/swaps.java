package testing23;

public class swaps {
	public static void main(String[] args) {
		int x=10;
		int y=20;
		
		System.out.println("before swap"+"\n"+"x = "+ x+"\n"+"y = "+ y);
		int temp = x;
		x=y;
		y=temp;
		System.out.println("after swaps"+"\n"+"x ="+ x+"\n"+"y ="+ y);
		
	}

}
